char *cap_string(char *s)
{
	char *temp = s;

	while (*s)
	{
		if (s == temp || *(s - 1) == ' ' || *(s - 1) == '\t' || *(s - 1) == '\n'
				|| *(s - 1) == ',' || *(s - 1) == ';' || *(s - 1) == '.'
				|| *(s - 1) == '!' || *(s - 1) == '?' || *(s - 1) == '"'
				|| *(s - 1) == '(' || *(s - 1) == ')' || *(s - 1) == '{'
				|| *(s - 1) == '}')
		{
			if (*s >= 97 && *s <= 122)
			{
				*s = *s - 32;
			}
		}
		s++;
	}
	
	return (temp);
}
